import { NextRequest, NextResponse } from "next/server";
import {
  ConsentStatus,
  WebhookEventType,
  FinancialInformationType,
} from "@/generated/prisma/client";
import { prisma } from "@/app/lib/prisma";
import { v4 as uuidv4 } from 'uuid';

interface WebhookPayload {
  type: string;
  success: boolean;
  consentId: string;
  timestamp: string;
  notificationId?: string;
  error?: any;
  data?: any;
  fiData?: any[];
  status?: string;
  dataRange?: {
    from: string;
    to: string;
  };
}

interface Transaction {
  mode: string;
  type: string;
  txnId?: string;
  amount: string;
  balance?: string;
  narration?: string;
  reference?: string;
  valueDate?: string;
  transactionTimestamp?: string;
  currentBalance?: string;
}

function mapFiType(raw: string | null | undefined): FinancialInformationType | null {
  if (!raw) return null;

  const upper = raw.toUpperCase(); // "deposit" -> "DEPOSIT"

  // make sure it's actually a valid enum value
  if ((Object.values(FinancialInformationType) as string[]).includes(upper)) {
    return upper as FinancialInformationType;
  }

  // fallback
  return FinancialInformationType.OTHER;
}


export async function POST(request: NextRequest) {
  const bodyText = await request.text();

  try {
    const payload = JSON.parse(bodyText) as WebhookPayload;
    const eventType = payload.type as WebhookEventType;
    const success =
      typeof payload.success === "boolean"
        ? payload.success
        : !!payload.success;

    // setu sends an external consent id (named consentId in payload) â€” find our consent row by that external id
    const externalConsentId =
      payload?.consentId ?? payload?.data?.consentId ?? null;

	console.log("consentID: ", payload?.consentId ?? payload?.data?.consentId)

    let linkedConsent = null;
    if (externalConsentId) {
      linkedConsent = await prisma.accountAggregatorConsent.findFirst({
        where: { consentId: String(externalConsentId) },
        select: { id: true },
      });
    }

    // Store webhook event
    const webhook = await prisma.setuWebhook.create({
      data: {
        id: uuidv4(),
        eventType: eventType,
        timestamp: new Date(payload.timestamp),
        success: success,
        consentId: linkedConsent ? linkedConsent.id : null,
        error: payload.error as any,
        payload: payload as any,
      },
    });

    // Handle different webhook types
    if (payload.type === "FI_DATA_FAILED") {
      // Ignore for now as requested
      return NextResponse.json({
        id: webhook.id,
        message: "FI_DATA_FAILED ignored",
      });
    } else if (payload.type === "FI_DATA_READY") {
      await handleFIDataReady(payload);
    } else if (payload.type === "CONSENT_STATUS_UPDATE") {
      await handleConsentStatusUpdate(payload);
    }

    return NextResponse.json(
      { id: webhook.id, success: true },
      { status: 200 }
    );
  } catch (error) {
    console.error("Failed to process Setu webhook", error);
    return NextResponse.json(
      {
        error:
          error instanceof Error ? error.message : "Failed to process webhook",
      },
      { status: 500 }
    );
  }
}

async function handleConsentStatusUpdate(payload: WebhookPayload) {
  const existingConsent = await prisma.accountAggregatorConsent.findFirst({
    where: {
      consentId: payload.consentId,
    },
  });

  if (!existingConsent) {
    console.warn(`Consent ${payload.consentId} not found`);
    return;
  }

  // Update consent status
  await prisma.accountAggregatorConsent.update({
    where: {
      id: existingConsent.id,
    },
    data: {
      status: payload.data?.status as ConsentStatus,
    },
  });

  if (payload.data && payload.data.status) {
    await prisma.customer.update({
      where: {
        id: existingConsent.userId,
      },
      data: {
        consent: payload.data.status === "ACTIVE" ? true : false,
      },
    });
  } 

  // Create financial accounts
  const accounts = payload.data?.detail?.accounts;
  if (accounts && Array.isArray(accounts)) {
    for (const account of accounts) {
      // Check if account already exists
      const existingAccount = await prisma.financialAccount.findFirst({
        where: {
          linkRefNumber: account.linkRefNumber,
          userId: existingConsent.userId,
        },
      });

      if (!existingAccount) {
        // Create new account
        const newAccount = await prisma.financialAccount.create({
          data: {
            id: uuidv4(),
            fipId: account.fipId,
            fipType: account.fiType as FinancialInformationType,
            accountType: account.accType,
            linkRefNumber: account.linkRefNumber,
            maskedAccNumber: account.maskedAccNumber,
            userId: existingConsent.userId,
          },
        });

        // Create consent-account mapping
        await prisma.consentAccountMapping.create({
          data: {
            consentId: existingConsent.id,
            accountId: newAccount.id,
          },
        });
      }
    }
  }
}

async function handleFIDataReady(payload: WebhookPayload) {
  const consent = await prisma.accountAggregatorConsent.findFirst({
    where: {
      consentId: payload.consentId,
    },
  });

  if (!consent) {
    console.warn(`Consent ${payload.consentId} not found for FI_DATA_READY`);
    return;
  }

  const fiDataArray = payload.fiData;
  if (!fiDataArray || !Array.isArray(fiDataArray)) {
    return;
  }

  // Process each FI data item
  for (const fiDataItem of fiDataArray) {
    const dataArray = fiDataItem.data;
    if (!dataArray || !Array.isArray(dataArray)) continue;

    for (const accountData of dataArray) {
      const { decryptedFI, linkRefNumber, maskedAccNumber } = accountData;

      if (!decryptedFI || !linkRefNumber) continue;

      // Find the financial account
      const financialAccount = await prisma.financialAccount.findFirst({
        where: {
          linkRefNumber: linkRefNumber,
          userId: consent.userId,
        },
      });

      if (!financialAccount) {
        console.warn(
          `Financial account with linkRefNumber ${linkRefNumber} not found`
        );
        continue;
      }

      // Extract account details
      const account = decryptedFI.account;

      // 1. Store Account Holders (Profile)
      await storeAccountHolders(financialAccount.id, account.profile);

      // 2. Store Account Summary
      await storeAccountSummary(
        financialAccount.id,
        account.summary,
        decryptedFI.type
      );

      // 3. Store Transactions
      await storeTransactions(
        consent.userId,
        financialAccount.id,
        account.transactions?.transaction,
        decryptedFI.type as FinancialInformationType
      );
    }
  }
}

async function storeAccountHolders(financialAccountId: string, profile: any) {
  if (!profile?.holders?.holder) return;

  const holders = profile.holders.holder;
  if (!Array.isArray(holders)) return;

  for (const holder of holders) {
    // Check if holder already exists
    const existingHolder = await prisma.financialAccountHolder.findFirst({
      where: {
        financialAccountId: financialAccountId,
        pan: holder.pan,
      },
    });

    if (!existingHolder) {
      await prisma.financialAccountHolder.create({
        data: {
          id: uuidv4(),
          financialAccountId: financialAccountId,
          name: holder.name || "",
          dob: holder.dob || "",
          pan: holder.pan || "",
          email: holder.email || "",
          mobile: holder.mobile || "",
          address: holder.address || "",
          nominee: holder.nominee || "",
          ckycCompliance: holder.ckycCompliance || "",
        },
      });
    }
  }
}

async function storeAccountSummary(
  financialAccountId: string,
  summary: any,
  accountType: string
) {
  if (!summary) return;

  // Check if summary already exists
  const existingSummary = await prisma.financialAccountSummary.findUnique({
    where: {
      financialAccountId: financialAccountId,
    },
  });

  const summaryData: any = {
    branch: summary.branch || null,
    ifsc: summary.ifsc || summary.ifscCode || null,
    micrCode: summary.micrCode || null,
    openingDate: summary.openingDate ? new Date(summary.openingDate) : null,
    currentValue: summary.currentValue || null,
    status: summary.status || null,

    // Deposit specific
    accountSubType: summary.type || null,
    pendingAmount: summary.pending?.amount || null,
    pendingTxnType: summary.pending?.transactionType || null,
    currency: summary.currency || null,
    facility: summary.facility || null,
    drawingLimit: summary.drawingLimit || null,
    currentBalance: summary.currentBalance || null,
    currentODLimit: summary.currentODLimit || null,
    balanceDateTime: summary.balanceDateTime
      ? new Date(summary.balanceDateTime)
      : null,

    // Term/Recurring deposit specific
    tenureDays: summary.tenureDays || null,
    tenureMonths: summary.tenureMonths || null,
    tenureYears: summary.tenureYears || null,
    interestRate: summary.interestRate || null,
    maturityDate: summary.maturityDate ? new Date(summary.maturityDate) : null,
    maturityAmount: summary.maturityAmount || null,
    principalAmount: summary.principalAmount || null,
    interestPayout: summary.interestPayout || null,
    interestComputation: summary.interestComputation || null,
    compoundingFrequency: summary.compoundingFrequency || null,

    // Recurring deposit specific
    recurringAmount: summary.recurringAmount || null,
    recurringDepositDay: summary.recurringDepositDay || null,
    interestOnMaturity: summary.interestOnMaturity || null,
    interestPeriodicPayoutAmount: summary.interestPeriodicPayoutAmount || null,
    description: summary.description || null,
  };

  if (existingSummary) {
    await prisma.financialAccountSummary.update({
      where: {
        financialAccountId: financialAccountId,
      },
      data: summaryData,
    });
  } else {
    await prisma.financialAccountSummary.create({
      data: {
        id: uuidv4(),
        ...summaryData,
        financialAccountId: financialAccountId,
      },
    });
  }
}

async function storeTransactions(
  userId: string,
  financialAccountId: string,
  transactions: Transaction[] | undefined,
  fiType: FinancialInformationType
) {
  if (!transactions || !Array.isArray(transactions)) return;

  for (const txn of transactions) {
    // Check if transaction already exists by txnId
    if (txn.txnId) {
      const existingTxn = await prisma.transaction.findFirst({
        where: {
          txnId: txn.txnId,
          financialAccountId: financialAccountId,
        },
      });

      if (existingTxn) {
        continue;
      }
    }

    // Parse amount and balance
    const amount = parseFloat(txn.amount || "0");
    const balance = txn.balance || txn.currentBalance;
    const balanceValue = balance ? parseFloat(balance) : null;

    // Determine transaction date
    let txnDate: Date;
    if (txn.transactionTimestamp) {
      txnDate = new Date(txn.transactionTimestamp);
    } else if (txn.valueDate) {
      txnDate = new Date(txn.valueDate);
    } else {
      txnDate = new Date();
    }

    const valueDate = txn.valueDate ? new Date(txn.valueDate) : null;

    await prisma.transaction.create({
      data: {
        id: uuidv4(),
        userId: userId,
        financialAccountId: financialAccountId,
        mode: txn.mode,
        type: txn.type,
        txnId: txn.txnId || null,
        amount: amount,
        balance: balanceValue,
        narration: txn.narration || null,
        reference: txn.reference || null,
        date: txnDate,
        valueData: valueDate,
         fiType: mapFiType(fiType), // <- key line
      },
    });
  }
}

